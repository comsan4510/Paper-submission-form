          <h1 kiji>ゲームセンターで景品をとる方法</h1>
          <p kiji>こんにちは　ジーヨです。</p>
          <p kiji>突然ですが、みなさんはゲームセンターのクレーンゲームを見て「あの景品とれないかなあ」ということを考えたことはないでしょうか？</p>
          <p kiji>私は大いにあります。今の私がクレーンゲームにハマるようになったのもそれがきっかけです。</p>
          <p kiji>しかし、「欲しくても欲しくても全然とれない！」「気づけば何千円も使っている...」という経験がある方は少なくないでしょう。</p>
          <p yohaku>私がクレーンゲームにハマり始めたときは、4000円近く使って何一つとして景品をとることはできませんでした...。</p>
          <p>しかし、最近はゲームセンターに行けば必ず1000円以内で少なくともフィギュアを1つとれるようになりました！</p>
          <p>そんな私が今回書く記事は、「私が考えるゲームセンターで景品をとる方法3選」です。</p>
          <p yohaku>※注意</p>
          <p>この記事は「1000円以内で景品を最低1つはとる」ということを目標とした記事です。</p>
          <p>この記事では、クレーンゲームなどの景品を獲得するゲームのことを総じて「プライズゲーム」と称します。</p>
          <p>そして、この記事の内容はあくまでも私個人の経験談であり、確たる証拠はないことをご了承ください。</p>
          <p>それでは、参考になったら幸いです。</p>
          <h1 setumei>1.在庫処分台を探す</h1>
          <p>1つ目は、余った在庫をお客さんにとってもらうプライズゲーム台、通称「在庫処分台」を探しましょう。在庫処分台は「お客さんにたくさんお金を使ってもらう」ことより、
            「早急な在庫の処分」を優先されたものなので、他のゲーム台よりも比較的とれやすくなっています。（店によっては在庫処分台でも、とりづらい場所もありますが...）</p>
          <p yohaku>この台によく置かれてる景品はフィギュアやアニメグッズが多いかと思います。逆に、賞味期限の近い飲食品が置かれることはまずないでしょう。</p>
          <p>そんな在庫処分台はどのようなゲームなのか？それは店によって様々です。クレーンゲーム以外に、透明な壁の隙間に棒を上手く差し込んで景品を落とすプッシャーゲームや、
            「トライポッド」などのルーレット型式のゲームを在庫処分に使っているところを見たことがあります。</p>
          <p>その中でも私がオススメするゲームはプッシャーゲームです。オススメする理由は、このゲームは「確率機」だということです。「確立機」とは、
            「投入金額が設定された額に達するまで景品の獲得を不可能とするプログラムが組み込まれている（wikiより）」ものを指しています。</p>
          <p yohaku>「決まった金額に到達するまで景品をとることができない」という点は一見すると、景品をとりやすいとは思えないかもしれませんが、逆に考えれば、お金を入れさえすれば必ずとれる瞬間が来るということです。そして、
            この確立機の設定金額は基本的に3000円だと言われていまが、在庫処分用に設定した場合、設定金額はもっと低くしあるものが存在します。</p>
          <p yohaku>ちなみに、私の行きつけのゲームセンターの在庫処分用の確立機の設定金額はおそらく1500円～2000円だと思われます。そう思う理由は、その確率機で景品を5回狙いました。</p>
          <p>1回1回の間に2、3日ほどの間をあけました。そうすると景品がとれるまで、1回目200円、2回目1400円、3回目300円、4回目800円、5回目500円というように、投入金額が1500円以上になることがなかったのです。それは、
            ただ運がいいだけと言われればそれまでですが、5回の投入金額の平均値が640円と、設定金額が3000円ではおそらくあり得ない数字だと思います。</p>
          <p>しかし、1500円を払ってしまうと上記の「1000円以内で少なくともフィギュアを1つとる」ということにはなりませんね。そこで「ハイエナ」をしましょう。</p>
          <h1 setumei>2．ハイエナ</h1>
          <p>2つ目は「ハイエナ」です。「ハイエナ」とは、他の人が景品をとろうとして諦めたゲームの台で景品をとることです。そうすることで、通常よりも安い出費で景品をとることができます。そしてプライズゲームの中でも「確率機」
            が「ハイエナ」をしやすいと思います。その理由は「確立機の設定金額」に関係があります。</p>
          <p yohaku>仮に確率機以外のクレーンゲームでハイエナをするとなると、通常よりも安い出費で景品をとれる可能性はあるが、それでも1000円を消費することも少なくないでしょう。一方で「確率機」の場合、景品がとれるタイミング
            （設定金額）はあらかじめ決まっているため、自分の前の人達の投入金額次第で自分がとれるかどうかが決まります。</p>
          <p yohaku>設定金額が3000円であれば、自分の番にとれるかは少し難しいですが、「在庫処分台」の1500円の設定であれば、自分の前の人たちの出費の合計が500円以上1400円以内であれば、1000円以内でとれることができます。</p>
          <p>もし近所のゲームセンターにプッシャーゲームを在庫処分台として使っている場所があれば一度は試してみてはどうでしょう。</p>
          <h1 setumei>3．待つ</h1>
          <p>最後の3つ目は「待つ」ことです。これはなにかというと、「新登場したこの景品がほしいけどとるのは難しい」というとき、一端落ち着いて待ってみましょう。もしその景品が「鬼滅の刃」並みの人気作品であるならば、
            すぐになくなるかもしれませんが、大抵のものは在庫を抱えるものです。そしてその景品の旬が過ぎたとき、「在庫処分台」を見てみてください。きっとそこに欲しいと思った景品があるでしょう。後は上記のような考えの元、
            景品を得るだけです。</p>
          <h1 setumei>まとめ</h1>
          <p>いかがでしたでしでしょうか。今回の記事を簡潔にまとめると「欲しい景品を見つけたら、在庫処分台（確率機が好ましい）に置かれるまで少し待って、なるべくハイエナを意識して取る」ということです。</p>
          <p>もちろん、景品の物によっては今回ご紹介した方法が通用しない場合もあります。例えば人気の景品の場合、すぐに在庫がなくなったり、いつまでも在庫処分台には入らないなんてことはざらにあります。</p>
          <p yohaku>「確率機」も場合によっては1000円以上使うでしょう。あくまでも今回は「1000円以内で景品を最低1つはとる」ことを目標とした1つの手段であり、「確実にとれる」という保証はありません。</p>
          <p>プライズゲームは「とれるかとれないかのドキドキ」を醍醐味としたゲームだと私は思います。景品が取れないことに躍起になるときもあるでしょう。そんなときだからこそ、
            その醍醐味を肩の力を抜いて楽しむこと前提にゲームをしてみてはいかがでしょうか。</p>
          <p yohaku>次は「在庫処分台はどうやって探すのか」という記事を書きます。</p>
