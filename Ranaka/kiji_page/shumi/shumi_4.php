これが私がプライズゲームで遊ぶ際の基本的な予算の使い方です。
要点は「+200円追加」と「500円6回」はその景品の欲しさによって使い分けているところです。

めちゃくちゃほしい景品では500円を、それほど欲しくはないけど取れそうな景品は200円を使っていきます。
（時々、熱くなって欲しいわけでもない景品にどんどんつぎ込むこともありますが…（笑））

もし、500円でまだ取れなかったときは、撤退するか、もういちど500円を入れます。

そして500円を合計3回入れても取れなかったときは撤退します。
「今日が実力も運もツイていない日だな」と割り切ることも大切です。

